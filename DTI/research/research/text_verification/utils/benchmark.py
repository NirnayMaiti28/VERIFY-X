def run_benchmark(pipeline):
    predictions = []
    expected = []
    bucket_counts = Counter()

    for sample in BENCHMARK_DATASET:
        claim = sample["claim"]
        exp = sample["expected"]
        bucket = sample["bucket"]

        try:
            result = pipeline.verify_claim(claim)
            pred = (result or {}).get("verdict", "Not Enough Information")
        except Exception:
            pred = "Not Enough Information"

        predictions.append(pred)
        expected.append(exp)
        bucket_counts[bucket] += 1

    correct = sum(1 for p, e in zip(predictions, expected) if p == e)
    total = len(expected)
    accuracy = (correct / total) if total else 0.0

    distribution = Counter(predictions)

    print("=== Benchmark Summary ===")
    print(f"Total claims: {total}")
    print(f"Accuracy: {accuracy:.2%}")
    print(f"Buckets: true={bucket_counts['true']}, false={bucket_counts['false']}, recent={bucket_counts['recent']}")
    print("Prediction counts:")
    print(f"- True: {distribution.get('True', 0)}")
    print(f"- False: {distribution.get('False', 0)}")
    print(f"- Misleading: {distribution.get('Misleading', 0)}")
    print(f"- Not Enough Information: {distribution.get('Not Enough Information', 0)}")

    return {
        "accuracy": round(accuracy, 4),
        "total": total,
        "correct": correct,
        "prediction_counts": {
            "True": distribution.get("True", 0),
            "False": distribution.get("False", 0),
            "Misleading": distribution.get("Misleading", 0),
            "Not Enough Information": distribution.get("Not Enough Information", 0),
        },
    }


